// Arithmetic Operators
let a = 10;
let b = 4;
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a / b = ", a / b);
console.log("a ** b = ", a ** b);
console.log("a % b = ", a % b);
console.log("++a = ", ++a);
console.log("a++ = ", a++);
console.log("--a = ", --a);
console.log("a-- = ", a--);
console.log("a = ", a);
console.log("a-- = ", a--);

// Assignment Operators
let assignment = 1;
assignment += 5; // same as assignment = assignment + 5
console.log("a is now = ", a);
assignment -= 5; // same as assignment = assignment - 5
console.log("a is now = ", a);
assignment *= 5; // same as assignment = assignment * 5
console.log("a is now = ", a);
assignment /= 5; // same as assignment = assignment / 5
console.log("a is now = ", a);

// Comparison Operators
let comp1 = 6;
let comp2 = 7;
console.log("comp1 == comp2 is ", comp1 == comp2); // It only checks value
console.log("comp1 != comp2 is ", comp1 != comp2);
console.log("comp1 === comp2 is ", comp1 === comp2); // It checks value as well as type
console.log("comp1 !== comp2 is ", comp1 !== comp2);
console.log("comp1 > comp2 is ", comp1 > comp2);

// Comparison Operators
